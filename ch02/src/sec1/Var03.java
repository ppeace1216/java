package sec1;

public class Var03 {

	public static void main(String[] args) {
		boolean a=true;	//true 또는 false만 입력가능
		//정수
		byte b=20, c=127;	//-128~127까지만 저장 가능 ->1byte(8bit = 2의 8승 = 256가지) 초과 오버플로우,미만 언더플로우
		short d=6450;	//2byte(16bit = 2의 16승 = 65536가지)
		int e=65000;	//4byte(32bit)
		long f=540000;	//8byte(64bit)
		//실수
		float g=3.14f;	//4byte(32bit)
		double h=3.14;	//8byte(64bit) float처럼 f를 뒤에 꼭 안붙여도 됨
		//문자/문자열
		char i='k';		//2byte(16bit)
		
		String J="kim";	//
	}

}
